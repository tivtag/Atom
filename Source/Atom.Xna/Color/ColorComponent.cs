
namespace Atom.Xna
{
    /// <summary>
    /// Enumerates the components of a RGBA-color.
    /// </summary>
    public enum ColorComponent
    {
        /// <summary>
        /// No specific component.
        /// </summary>
        None,

        /// <summary>
        /// The red component.
        /// </summary>
        Red,

        /// <summary>
        /// The green component.
        /// </summary>
        Green,

        /// <summary>
        /// The blue component.
        /// </summary>
        Blue,

        /// <summary>
        /// The alpha/transparency component.
        /// </summary>
        Alpha
    }
}
